model_new.fit(x= eng_tokenized_padded, y=fr_tokenized_padded, validation_split=0.2, epochs = 25, batch_size = 64)

"""***Generating Translations from English to French***

Once the model is built and trained, the next step is to define a function that accepts an input sentence in English and produces the corresponding translated sentence in French.
"""

def final_predictions_model1(sentence):

  y_id_to_word = {value: key for key, value in fr_tokenizer.word_index.items()}
  y_id_to_word [0] = '<PAD>'

  sentence = eng_tokenizer.texts_to_sequences([sentence])

  sentence = pad_sequences(sentence, maxlen = max_eng, padding = 'post')

