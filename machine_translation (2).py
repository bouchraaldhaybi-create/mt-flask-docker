"""***Identifying Maximum Sentence Lengths***

French sentences generally appear longer than their English counterparts. For instance, English sentences often cap at around 14 words, while French ones can easily reach 20.

To improve accuracy, we'll determine the maximum word count for each language separately.
"""

max_eng = df['English_len'].max()
max_fr = df['French_len'].max()
print('So the English Sentence that has the greatest number of words is made of', max_eng, 'words, while the French Sentence that has the greatest number of words is made of ', max_fr, ' words')

"""# Preprocessing Data


Before processing the data, we need to tokenize and pad it appropriately. Since the dataset includes both English and French texts, we cannot use a single tokenizer for both languages.

Each tokenizer is designed to handle a specific language and relies on a unique vocabulary or dictionary tailored to that language. These dictionaries cannot be combined or merged.

🔹 Therefore, we must use:

* An English-specific tokenizer for English data

* A French-specific tokenizer for French data
"""

from tensorflow.keras.preprocessing.text import Tokenizer

eng_tokenizer = Tokenizer()
eng_tokenizer.fit_on_texts(df['English'])

fr_tokenizer = Tokenizer()
fr_tokenizer.fit_on_texts(df['French'])

"""***Converting Text into Numerical Sequences***

Once the data is tokenized, the next step is to fit it onto the dataset. This involves transforming the text using the texts_to_sequences method, which converts each word into its corresponding numerical representation based on the tokenizer’s vocabulary.

 In other words:

* Every word is mapped to a specific number

* This transformation prepares the data for input into machine learning models
"""

fr_tokenized = fr_tokenizer.texts_to_sequences(df['French'])
eng_tokenized = eng_tokenizer.texts_to_sequences(df['English'])

"""***Determining the Vocabulary Size per Language***


To identify the number of unique words in both English and French, we need to access the word_index attribute of each tokenizer individually.

✅ That means:

* Use the English tokenizer's word_index to count unique English tokens

* Use the French tokenizer's word_index to count unique French tokens

Each tokenizer builds its own vocabulary based on the data it's trained on, so this step helps us understand the scope of each language's representation in the dataset.
"""

eng_vocab_size = len(eng_tokenizer.word_index)
fr_vocab_size = len(fr_tokenizer.word_index)
print("English Vocabulary Size:", eng_vocab_size)
print("French Vocabulary Size:", fr_vocab_size)

"""***Padding Sequences for Uniform Length***

To ensure all sentences have the same length, we’ll apply padding — specifically post-padding, where zeros are added at the end of each sentence.

Since English sentences reach a maximum of 15 words and French ones up to 21, it's logical to pad each set to its respective maximum length. This way, we preserve the full sentence structure while standardizing the sequence lengths for model training or analysis.
"""

import tensorflow as tf
from tensorflow.keras.preprocessing.sequence import pad_sequences

fr_tokenized_padded = pad_sequences(fr_tokenized, maxlen = max_fr, padding = 'post')
eng_tokenized_padded = pad_sequences(eng_tokenized, maxlen = max_eng, padding = 'post')

"""# Modeling

***Building a Seq2Seq Architecture***

After preprocessing the data, we can proceed to design the model using recurrent layers such as RNNs, LSTMs, or GRUs. A good starting point is exploring how to construct a Sequence-to-Sequence (Seq2Seq) model, which introduces key components and specialized layers for translation tasks.

The core idea of this architecture is to take an English sentence as input and generate its French translation as output.

🔹 Encoder

Embedding Layer:

Accepts the English sentence and maps each word to its corresponding embedding vector.

We specify:

The vocabulary size

The embedding dimension

The input sentence length

GRU Layer (or LSTM/RNN):

Processes the embeddings and produces a context vector summarizing the sentence.

This marks the end of the encoder phase.

🔸 Decoder
RepeatVector Layer:

Takes the context vector and repeats it for the number of time steps equal to the maximum French sentence length.

GRU Layer:

Predicts the output sequence step by step across the repeated context vector.

TimeDistributed Dense Layer + Softmax:

At each time step, a word in French is predicted.

The Dense layer (shared across all time steps) applies a softmax over the entire French vocabulary to select the most appropriate word.
"""

import numpy as np
from tensorflow.keras.models import Sequential
from tensorflow.keras.layers import Embedding, GRU, RepeatVector, TimeDistributed, Dense


test_model = Sequential()
test_model.add(Embedding(eng_vocab_size+1, 100, input_length=max_eng))
test_model.add(GRU(20))
test_model.add(RepeatVector(max_fr))
test_model.add(GRU(20, return_sequences=True))
test_model.add(TimeDistributed(Dense(fr_vocab_size+1, activation='softmax')))
test_model.summary()


model = Sequential()

# Input layer with explicit shape
model.add(Embedding(
    input_dim=eng_vocab_size,
    output_dim=100,  # Increased embedding dimension
    input_length=max_eng
))

model.add(GRU(20))
model.add(RepeatVector(max_fr))
model.add(GRU(20, return_sequences=True))
model.add(TimeDistributed(Dense(fr_vocab_size+1, activation='softmax')))

# 4. Force model build with concrete batch size
model.build(input_shape=(32, max_eng))  # 32 = typical batch size

# 5. Final summary
model.summary()

"""***Compiling and Training the Translation Model***

When compiling the model, we use sparse categorical crossentropy as the loss function. This choice is appropriate because our target sequences (the output labels) are represented as integers rather than one-hot encoded vectors. Sparse categorical crossentropy handles this format directly, offering faster computation—though it may sacrifice a bit of accuracy compared to its standard counterpart.

For training, we fit the model on our training dataset while reserving a validation split. This helps monitor the model’s performance and allows for fine-tuning based on validation feedback.

📉 A quick note on evaluation: Although accuracy is often visualized during training, it's not ideal for translation models. That’s because accuracy demands an exact match between predicted and reference sentences. Even a single-word difference would result in a failed match, making it too strict for evaluating translations.

Instead, alternative metrics like BLEU, ROUGE, or METEOR are commonly used in machine translation to measure how close a generated sentence is to the human reference.
"""

model.compile(optimizer = 'adam', loss = 'sparse_categorical_crossentropy', metrics = ['accuracy'])
model.fit(x= eng_tokenized_padded, y=fr_tokenized_padded, validation_split=0.2, epochs = 25, batch_size = 64)

"""***Alternative Loss Function Strategy***

Alternatively, we can apply one-hot encoding to our target sequences and use the standard categorical crossentropy as the loss function. This method represents each token with a vector where the correct class is marked with a 1 and the rest are zeros. It tends to be more precise in certain cases, especially when dealing with multi-class classification tasks—though it's slightly more computationally demanding than sparse categorical crossentropy
"""

from tensorflow.keras.utils import to_categorical

